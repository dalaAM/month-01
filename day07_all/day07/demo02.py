"""
    集合set
        去重复
"""
# 1. 创建
# set01 = {} # 是字典  不是集合
set01 = {"北京", "上海", "黑龙江"}
print(set01)  # {'上海', '黑龙江', '北京'}

list01 = ["北京", "北京", "北京"]
set02 = set(list01)
print(set02)  # {'北京'}

# 2. 添加
set01.add("重庆")
set01.add("上海")  # 如果添加已经存在的元素,集合中仍然只有一份
print(set01)

# 3. 删除
set01.remove("北京")
