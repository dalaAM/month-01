"""
    函数参数
        实际参数
            位置实参:实参与形参按照顺序对应
                序列实参:拆后按照顺序对应
            关键字实参:实参与形参按照名称对应
                字典实参:拆后按照名称对应
        形式参数
            默认形参:实参可选
"""


def func01(p1, p2, p3):
    print(p1)
    print(p2)
    print(p3)


list01 = [1, 2, 3]
func01(*list01)# 序列实参:拆 后按顺序对应

dict02 = {"p1": 1, "p2": 2, "p3": 3}
func01(**dict02)# 字典实参:拆 后按名称对应
